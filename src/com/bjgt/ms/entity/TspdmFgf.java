package com.bjgt.ms.entity;

import java.sql.Timestamp;

import com.bjgt.ms.entity.vo.BaseObject;

/**
 * 商品代码表-非规范
 */

public class TspdmFgf extends BaseObject {

	private static final long serialVersionUID = 3247124459893L;
	private Integer id;
	private String fspname;// 商品名称
	private String fspeng;// 英文
	private String fspdm;// 商品代码
	private String fgroup;// 分组
	private String page;// 页码
	private Timestamp fdate;//

	@Override
	public String toString() {
		return "TspdmFgf [id=" + id + ", fspname=" + fspname + ", fspeng="
				+ fspeng + ", fspdm=" + fspdm + ", fgroup=" + fgroup
				+ ", page=" + page + ", fdate=" + fdate + "]";
	}

	public TspdmFgf(Integer id, String fspname, String fspeng, String fspdm,
			String fgroup,String  page, Timestamp fdate) {
		super();
		this.id = id;
		this.fspname = fspname;
		this.fspeng = fspeng;
		this.fspdm = fspdm;
		this.fgroup = fgroup;
		this.page = page;
		this.fdate = fdate;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getFspname() {
		return fspname;
	}

	public void setFspname(String fspname) {
		this.fspname = fspname;
	}

	public String getFspeng() {
		return fspeng;
	}

	public void setFspeng(String fspeng) {
		this.fspeng = fspeng;
	}

	public String getFspdm() {
		return fspdm;
	}

	public void setFspdm(String fspdm) {
		this.fspdm = fspdm;
	}

	public String getFgroup() {
		return fgroup;
	}

	public void setFgroup(String fgroup) {
		this.fgroup = fgroup;
	}

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public Timestamp getFdate() {
		return fdate;
	}

	public void setFdate(Timestamp fdate) {
		this.fdate = fdate;
	}

	public TspdmFgf() {
		super();
	}

}
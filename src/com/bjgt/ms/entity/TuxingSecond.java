package com.bjgt.ms.entity;

import com.bjgt.ms.entity.vo.BaseObject;

/**
 * 图素二级目录
 * @author tom
 *
 */
public class TuxingSecond extends BaseObject {

	private static final long serialVersionUID = 251273882122321L;
	private String name;//图素名称
	private String code;//编号
	private String upcode;//上级编号

	public TuxingSecond() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getUpcode() {
		return upcode;
	}

	public void setUpcode(String upcode) {
		this.upcode = upcode;
	}

	public TuxingSecond(String name, String code, String upcode) {
		super();
		this.name = name;
		this.code = code;
		this.upcode = upcode;
	}

	@Override
	public String toString() {
		return "TuxingSecond [name=" + name + ", code=" + code + ", upcode="
				+ upcode + "]";
	}

}